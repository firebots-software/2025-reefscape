package frc.robot.commands;

import frc.robot.subsystems.LEDsubsystem;
import edu.wpi.first.wpilibj2.command.Command;

/** An example command that uses an example subsystem. */
public class LEDcommand extends Command {
  @SuppressWarnings({"PMD.UnusedPrivateField", "PMD.SingularField"})
  private LEDsubsystem m_LEDsubsystem;
  private CANdle candle;
  private CANdleConfiguration config;
  private ErrorCode error; // gets the last error generated by the CANdle
  private CANdleFaults faults;
  private finalErrorCode faultsError; // fills faults with the current CANdle faults; returns the last error generated

  /**
   * Creates a new ExampleCommand.
   *
   * @param subsystem The subsystem used by this command.
   */
  public LEDcommand(LEDsubsystem subsystem) {
    m_LEDsubsystem = subsystem;
    // Use addRequirements() here to declare subsystem dependencies.

    config.stripType = LEDStripType.RGB; // set the strip type to RGB
    config.brightnessScalar = 0.5; // dim the LEDs to half brightness
    candle.configAllSettings(config);

    candle = new CANdle(5);
    config = new CANdleConfiguration();
    error = candle.getLastError();
    faults = new CANdleFaults();
    faultsError = candle.getFaults(faults);

    RainbowAnimation rainbowAnim = new RainbowAnimation(1, 0.5, 64);

    addRequirements(subsystem);
  }

  // Called when the command is initially scheduled.
  @Override
  public void initialize() {}

  // Called every time the scheduler runs while the command is scheduled.
  @Override
  public void execute() {
    candle.setLEDs(255, 255, 255);
    candle.animate(rainbowAnim);
  }
     
  // Called once the command ends or is interrupted.
  @Override
  public void end(boolean interrupted) {}

  // Returns true when the command should end.
  @Override
  public boolean isFinished() {
    return false;
  }
}
